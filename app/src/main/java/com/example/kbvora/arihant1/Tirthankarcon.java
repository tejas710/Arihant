public class Tirthankarcon {

   private String tirthankar;
    private String photo1;

    public Tirthankarcon(String tirthankar, String photo1) {
        this.tirthankar = tirthankar;
        this.photo1 = photo1;
    }

    public String getTirthankar() {
        return tirthankar;
    }

    public void setTirthankar(String tirthankar) {
        this.tirthankar = tirthankar;
    }

    public String getPhoto1() {
        return photo1;
    }

    public void setPhoto1(String photo1) {
        this.photo1 = photo1;
    }
}
